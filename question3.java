    private void jbtnSubmitNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSubmitNextActionPerformed
        // TODO add your handling code here:
        if (!jrbtn1.isSelected() && !jrbtn2.isSelected() && !jrbtn3.isSelected() && !jrbtn4.isSelected()) {
            JOptionPane.showMessageDialog(null,"Please select an anwer");
        } else{
            //check correct answer
            if(jrbtn4.isSelected()){
                //assign 5 marks to marks variable
                marks = marks + 5;

                //assign questioned answered
                quizanswered = quizanswered + 1;
                //call next question window
                new question4(quizanswered, marks).setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jbtnSubmitNextActionPerformed

    private void jbtnCheckAnwerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCheckAnwerActionPerformed
        // check the user select an option. otherwise show an error message
        if (!jrbtn1.isSelected() && !jrbtn2.isSelected() && !jrbtn3.isSelected() && !jrbtn4.isSelected()) {
            JOptionPane.showMessageDialog(null,"Please select an anwer");

        }else{
            //code what happen when user select an right option
            if(jrbtn4.isSelected()){
                lblAnswerStatus.setText("Correct Answer");
//                lblAnswerStatus.setForeground(color.green);
            }else{
                lblAnswerStatus.setText("inCorrect - Answer is 1954");
//                lblAnswerStatus.setForeground(color.red);

            }
            //code to lock the all radio buttons when submit check answers
            jrbtn1.setEnabled(false);
            jrbtn2.setEnabled(false);
            jrbtn3.setEnabled(false);
            jrbtn4.setEnabled(false);
        }
    }//GEN-LAST:event_jbtnCheckAnwerActionPerformed

